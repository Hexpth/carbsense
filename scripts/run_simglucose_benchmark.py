"""
Reference benchmark: how well does the CarbSense MAP estimator recover the
true meal size D from CGM traces generated by SimGlucose (UVA/Padova S2013)?

Usage
-----
    python scripts/run_simglucose_benchmark.py
    python scripts/run_simglucose_benchmark.py --patients adult#001 adult#002
    python scripts/run_simglucose_benchmark.py --carbs 30 60 90 --out results.csv

Outputs a per-episode CSV plus a summary table printed to stdout. This is the
file you will commit to ``docs/benchmark_simglucose.md`` and that will become
Table 1 of the eventual preprint.

This is *not* a pytest test — it takes a few minutes per patient and we don't
want it slowing down ``pytest -v``. The pytest version is the lighter
``tests/test_against_simglucose.py`` which runs a single tiny episode.
"""

from __future__ import annotations

import argparse
import csv
import sys
import time
from dataclasses import dataclass
from pathlib import Path

import numpy as np

from carbsense.data.simglucose_bridge import basal_only_infusions, generate_meal_episode
from carbsense.inverse.map_estimator import estimate_meal
from carbsense.physiology.uvapadova_min import PatientParams

DEFAULT_PATIENTS = [f"adult#{i:03d}" for i in range(1, 11)]
DEFAULT_CARBS = [30.0, 50.0, 75.0, 100.0]


@dataclass
class EpisodeResult:
    patient: str
    D_true: float
    D_est: float
    t_meal_true: float
    t_meal_est: float
    abs_err: float
    rel_err: float
    final_loss: float
    converged: bool
    elapsed_s: float


def run_one_episode(
    patient: str,
    D_true: float,
    t_meal: float = 30.0,
    duration: int = 180,
    seed: int = 0,
) -> EpisodeResult:
    t0 = time.perf_counter()
    episode = generate_meal_episode(
        patient_name=patient,
        D_true=D_true,
        t_meal_minutes=t_meal,
        duration_minutes=duration,
        seed=seed,
    )

    params = PatientParams()
    infusions = basal_only_infusions()

    result = estimate_meal(
        y_obs=episode.cgm_mgdl,
        t_eval=episode.t_eval,
        params=params,
        infusions=infusions,
        G0=episode.G0,
        D_init=40.0,
        t_meal_init=t_meal - 10.0,  # warm start within ±10 min of true onset
        n_iter=400,
        lr=0.1,
    )

    elapsed = time.perf_counter() - t0
    abs_err = abs(result.D_est - D_true)
    return EpisodeResult(
        patient=patient,
        D_true=D_true,
        D_est=result.D_est,
        t_meal_true=t_meal,
        t_meal_est=result.t_meal_est,
        abs_err=abs_err,
        rel_err=abs_err / D_true,
        final_loss=result.final_loss,
        converged=result.converged,
        elapsed_s=elapsed,
    )


def summarise(results: list[EpisodeResult]) -> dict[str, float]:
    abs_errs = np.array([r.abs_err for r in results])
    rel_errs = np.array([r.rel_err for r in results])
    return {
        "n_episodes": float(len(results)),
        "mae_g": float(abs_errs.mean()),
        "median_ae_g": float(np.median(abs_errs)),
        "p90_ae_g": float(np.percentile(abs_errs, 90)),
        "mean_rel_err": float(rel_errs.mean()),
        "max_ae_g": float(abs_errs.max()),
    }


def print_summary(results: list[EpisodeResult], summary: dict[str, float]) -> None:
    print("\n" + "=" * 72)
    print("PER-EPISODE RESULTS")
    print("=" * 72)
    print(f"{'patient':<16}{'D_true':>8}{'D_est':>10}{'|err|':>8}{'rel%':>8}{'sec':>7}")
    print("-" * 72)
    for r in results:
        flag = "" if r.converged else " *"
        print(
            f"{r.patient:<16}"
            f"{r.D_true:>8.1f}"
            f"{r.D_est:>10.2f}"
            f"{r.abs_err:>8.2f}"
            f"{100 * r.rel_err:>7.1f}%"
            f"{r.elapsed_s:>7.1f}{flag}"
        )

    print("\n" + "=" * 72)
    print("SUMMARY")
    print("=" * 72)
    print(f"  Episodes:      {int(summary['n_episodes'])}")
    print(f"  MAE:           {summary['mae_g']:.2f} g")
    print(f"  Median AE:     {summary['median_ae_g']:.2f} g")
    print(f"  90th pct AE:   {summary['p90_ae_g']:.2f} g")
    print(f"  Max AE:        {summary['max_ae_g']:.2f} g")
    print(f"  Mean rel err:  {100 * summary['mean_rel_err']:.1f}%")
    print()
    print("Interpretation:")
    print("  MAE < 15 g  → simplified Bergman model is sufficient; proceed to NN.")
    print("  MAE 15–25 g → marginal; consider extending to full UVA/Padova S2013.")
    print("  MAE > 25 g  → significant model mismatch; debug forward operator.")
    print()


def save_csv(results: list[EpisodeResult], path: Path) -> None:
    with path.open("w", newline="") as f:
        writer = csv.DictWriter(
            f,
            fieldnames=[
                "patient",
                "D_true",
                "D_est",
                "t_meal_true",
                "t_meal_est",
                "abs_err",
                "rel_err",
                "final_loss",
                "converged",
                "elapsed_s",
            ],
        )
        writer.writeheader()
        for r in results:
            writer.writerow(r.__dict__)
    print(f"Per-episode results written to {path}")


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--patients",
        nargs="+",
        default=DEFAULT_PATIENTS,
        help="SimGlucose patient names (default: adult#001..adult#010)",
    )
    parser.add_argument(
        "--carbs",
        nargs="+",
        type=float,
        default=DEFAULT_CARBS,
        help="Meal sizes in grams (default: 30 50 75 100)",
    )
    parser.add_argument("--t-meal", type=float, default=30.0)
    parser.add_argument("--duration", type=int, default=180)
    parser.add_argument("--seed", type=int, default=0)
    parser.add_argument(
        "--out",
        type=Path,
        default=Path("benchmark_simglucose.csv"),
        help="CSV output path (default: benchmark_simglucose.csv)",
    )
    args = parser.parse_args()

    print("Running SimGlucose benchmark")
    print(f"  patients:  {args.patients}")
    print(f"  carbs (g): {args.carbs}")
    print(f"  episodes:  {len(args.patients) * len(args.carbs)}")
    print()

    results: list[EpisodeResult] = []
    total = len(args.patients) * len(args.carbs)
    done = 0
    for patient in args.patients:
        for D in args.carbs:
            done += 1
            print(f"[{done:>3}/{total}] {patient}  D={D:>5.1f} g ... ", end="", flush=True)
            try:
                r = run_one_episode(
                    patient=patient,
                    D_true=D,
                    t_meal=args.t_meal,
                    duration=args.duration,
                    seed=args.seed,
                )
            except Exception as exc:  # noqa: BLE001
                print(f"FAILED: {exc}")
                continue
            print(f"D_est={r.D_est:>6.2f}  |err|={r.abs_err:>5.2f}  ({r.elapsed_s:.1f} s)")
            results.append(r)

    if not results:
        print("No episodes completed successfully.", file=sys.stderr)
        return 1

    summary = summarise(results)
    print_summary(results, summary)
    save_csv(results, args.out)
    return 0


if __name__ == "__main__":
    sys.exit(main())
